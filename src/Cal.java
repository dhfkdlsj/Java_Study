public class Cal {
    int plus(int x, int y){
        int sum = x+y;
        return sum;
    } // int타입 매개변수 2개

    double plus(double x, double y){
        double sum = x+y;
        return sum;
    } // double타입 처리

    int plus(int x, int y, int z){
        int sum = x+y+z;
        return sum;
    } //int타입 매개변수 3개
}